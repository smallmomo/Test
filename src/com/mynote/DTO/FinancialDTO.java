package com.mynote.DTO;

public class FinancialDTO {
	private int f_id;
	private String f_type;
	private double f_money;
	private String f_other;
	private double f_allmoney;
	private String f_time;
	private Integer f_uid;
	public int getF_id() {
		return f_id;
	}
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	public String getF_type() {
		return f_type;
	}
	public void setF_type(String f_type) {
		this.f_type = f_type;
	}
	public double getF_money() {
		return f_money;
	}
	public void setF_money(double f_money) {
		this.f_money = f_money;
	}
	public String getF_other() {
		return f_other;
	}
	public void setF_other(String f_other) {
		this.f_other = f_other;
	}
	public double getF_allmoney() {
		return f_allmoney;
	}
	public void setF_allmoney(double f_allmoney) {
		this.f_allmoney = f_allmoney;
	}
	public String getF_time() {
		return f_time;
	}
	public void setF_time(String f_time) {
		this.f_time = f_time;
	}
	public Integer getF_uid() {
		return f_uid;
	}
	public void setF_uid(Integer f_uid) {
		this.f_uid = f_uid;
	}
	
}
